using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class FloatExtends
{

    /// <summary>
    /// 参数1除以参数2  得到float值
    /// </summary>
    /// <param name="number"></param>
    /// <param name="number2"></param>
    /// <returns></returns>
    public static float DivisionFloat(int number,int number2)
    {
        return ((float) number / (float) (number2));
    }
}
